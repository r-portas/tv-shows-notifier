class TvSeries{
    public String name;
    public String showid;
    
    TvSeries(String n, String id){
        name = n;
        showid = id;
    }
 
    JSONObject generateJson(){
        JSONObject temp = new JSONObject();
        temp.put("name", name);
        temp.put("showid", showid);
        return temp;
    }
}

